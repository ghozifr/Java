package com;

public class main {
    public static void main(String[] args) {
        /*

        integer, byte, short, longm double, float, char, boolean
        (primitive)
        (string is high level data type so not include)
        */

        // integer (unit)
        int i = 10;
        System.out.println("======INTEGER======");
        System.out.println("value of integer i = " + i);
        System.out.println("Maximum value of integer is " + Integer.MAX_VALUE);
        System.out.println("Minimum value of integer is " + Integer.MIN_VALUE);
        System.out.println("Size of integer = " + Integer.BYTES + " bytes");
        System.out.println("Size of integer = " + Integer.SIZE + " bit");
        System.out.println(" ");

        // byte (unit)
        byte b = 10;
        System.out.println("======BYTE======");
        System.out.println("value of byte b = " + b);
        System.out.println("Maximum value of byte is " + Byte.MAX_VALUE);
        System.out.println("Minimum value of byte is " + Byte.MIN_VALUE);
        System.out.println("Size of byte = " + Byte.BYTES + " bytes");
        System.out.println("Size of byte = " + Byte.SIZE + " bit");
        System.out.println(" ");

        // short (unit)
        short s = 10;
        System.out.println("======SHORT======");
        System.out.println("value of short s = " + s);
        System.out.println("Maximum value of short is " + Short.MAX_VALUE);
        System.out.println("Minimum value of short is " + Short.MIN_VALUE);
        System.out.println("Size of short = " + Short.BYTES + " bytes");
        System.out.println("Size of short = " + Short.SIZE + " bit");
        System.out.println(" ");

        // long (unit)
        long l = 10L;
        System.out.println("======LONG======");
        System.out.println("value of long l = " + l);
        System.out.println("Maximum value of long is " + Long.MAX_VALUE);
        System.out.println("Minimum value of long is " + Long.MIN_VALUE);
        System.out.println("Size of long = " + Long.BYTES + " bytes");
        System.out.println("Size of long = " + Long.SIZE + " bit");
        System.out.println(" ");

        // double (comma, real number)
        double d = 10.5d;
        System.out.println("======DOUBLE======");
        System.out.println("value of double d = " + d);
        System.out.println("Maximum value of double is " + Double.MAX_VALUE);
        System.out.println("Minimum value of double is " + Double.MIN_VALUE);
        System.out.println("Size of double = " + Double.BYTES + " bytes");
        System.out.println("Size of double = " + Double.SIZE + " bit");
        System.out.println(" ");

        // float (comma, real number)
        float f = 10.5f;
        System.out.println("======FLOAT======");
        System.out.println("value of float d = " + d);
        System.out.println("Maximum value of float is " + Float.MAX_VALUE);
        System.out.println("Minimum value of float is " + Float.MIN_VALUE);
        System.out.println("Size of float = " + Float.BYTES + " bytes");
        System.out.println("Size of float = " + Float.SIZE + " bit");
        System.out.println(" ");

        // char (alphabet) based on ASCII
        char c = 'c';
        System.out.println("======CHAR======");
        System.out.println("value of char c = " + c);
        System.out.println("Maximum value of char is " + Character.MAX_VALUE);
        System.out.println("Minimum value of char is " + Character.MIN_VALUE);
        System.out.println("Size of char = " + Character.BYTES + " bytes");
        System.out.println("Size of char = " + Character.SIZE + " bit");
        System.out.println(" ");

        // boolean (t/f)
        boolean val = true;
        System.out.println("======BOOLEAN======");
        System.out.println("value of boolean val = " + val);
        System.out.println("Maximum value of boolean is " + Boolean.TRUE);
        System.out.println("Minimum value of boolean is " + Boolean.FALSE);
        System.out.println(" ");

        
    }
}
